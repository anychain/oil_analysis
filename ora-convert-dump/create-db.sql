-- Create a tablespace for isolation.
-- DROP TABLESPACE DBNAME INCLUDING CONTENTS AND DATAFILES;
CREATE BIGFILE TABLESPACE DBNAME DATAFILE 'DBNAME.dat' SIZE 500M AUTOEXTEND ON MAXSIZE UNLIMITED;
-- Create the user.
CREATE USER oil IDENTIFIED BY passw0rd DEFAULT TABLESPACE DBNAME;
CREATE USER PDQ_OWNR IDENTIFIED BY passw0rd DEFAULT TABLESPACE DBNAME;
CREATE USER PDQ_LDR IDENTIFIED BY passw0rd DEFAULT TABLESPACE DBNAME;
CREATE USER EWA_OGM_READ IDENTIFIED BY passw0rd DEFAULT TABLESPACE DBNAME;
CREATE USER PDQ_USR IDENTIFIED BY passw0rd DEFAULT TABLESPACE DBNAME;
-- Grant permissions.
GRANT EXECUTE ON UTL_FILE to oil;
GRANT CREATE SESSION,CREATE SYNONYM,CONNECT,RESOURCE,CREATE VIEW,IMP_FULL_DATABASE to oil;
GRANT CREATE SESSION,CREATE SYNONYM,CONNECT,RESOURCE,CREATE VIEW,IMP_FULL_DATABASE to PDQ_OWNR;
GRANT CREATE SESSION,CREATE SYNONYM,CONNECT,RESOURCE,CREATE VIEW,IMP_FULL_DATABASE to PDQ_LDR;
GRANT CREATE SESSION,CREATE SYNONYM,CONNECT,RESOURCE,CREATE VIEW,IMP_FULL_DATABASE to EWA_OGM_READ;
GRANT CREATE SESSION,CREATE SYNONYM,CONNECT,RESOURCE,CREATE VIEW,IMP_FULL_DATABASE to PDQ_USR;
-- end the sqlplus session so that the ssh command returns.
quit;

